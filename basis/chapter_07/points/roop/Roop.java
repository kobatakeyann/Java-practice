class Roop {
    public static void main(String[] args) {
        // do-while文
        int cnt = 0;
        do {
            System.out.println(cnt);
            cnt++;
        } while (cnt < 5);
        // for文
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
    }
}
