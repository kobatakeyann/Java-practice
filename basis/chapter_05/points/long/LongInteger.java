class LongInteger {
    public static void main(String[] args) {
        // long型
        long longNumber = 1235999988766L;
        System.out.println(longNumber);
        // _をつけることもできる
        long longNumberWithUnderscore = 32_300_233_113_000L;
        System.out.println(longNumberWithUnderscore);
    }
}
